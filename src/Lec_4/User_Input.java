package Lec_4;
import java.util.*;
public class User_Input {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        byte c=sc.nextByte();
        System.out.println(c);
        int d=sc.nextInt();
        System.out.println(d);
        char ch=sc.next().charAt(0);
        System.out.println(ch);
    }
}
