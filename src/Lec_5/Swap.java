package Lec_5;

public class Swap {
    public static void main(String[] args) {
        int a=1;
        System.out.println("a: " + a);
        int b=2;
        System.out.println("b: " + b);
        int c=a;
        a=b;
        b=c;
        System.out.println("current a: " +a);
        System.out.println("current b: " +b);
    }
}
